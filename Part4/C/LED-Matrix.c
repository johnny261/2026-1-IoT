// MAX7219
// ENVIO DATOS
void max7219_send_all(uint8_t reg, uint8_t *data_array)
{
    spi_transaction_t t;
    uint8_t buffer[NUM_MODULES * 2];
    int idx = 0;
    for (int i = NUM_MODULES - 1; i >= 0; i--)
    {
        buffer[idx++] = reg;
        buffer[idx++] = data_array[i];
    }
    memset(&t, 0, sizeof(t));
    t.length = NUM_MODULES * 16;
    t.tx_buffer = buffer;
    gpio_set_level(PIN_NUM_CS, 0);
    spi_device_transmit(spi, &t);
    gpio_set_level(PIN_NUM_CS, 1);
}

void max7219_init()
{
    uint8_t data[NUM_MODULES];
    memset(data, 0x00, NUM_MODULES);
    max7219_send_all(0x0F, data); // test off
    memset(data, 0x01, NUM_MODULES);
    max7219_send_all(0x0C, data); // normal mode
    memset(data, 0x07, NUM_MODULES);
    max7219_send_all(0x0B, data); // scan limit
    memset(data, 0x04, NUM_MODULES);
    max7219_send_all(0x0A, data); // intensidad baja
    memset(data, 0x00, NUM_MODULES);
    max7219_send_all(0x09, data); // no decode
}

// FRAMEBUFFER
void clear_buffer()
{
    memset(framebuffer, 0, sizeof(framebuffer));
}

void draw_pixel(int x, int y, int val)
{
    if (x < 0 || x >= WIDTH || y < 0 || y >= 8) return;
    framebuffer[y][x] = val;
}

// TEXTO
const uint8_t* get_char(char c)
{
    for (int i = 0; i < sizeof(font)/sizeof(font_t); i++)
        if (font[i].c == c)
            return font[i].data;
    return font[sizeof(font)/sizeof(font_t)-1].data;
}

void draw_char(int x, char c)
{
    const uint8_t *ch = get_char(c);
    for (int col = 0; col < 5; col++)
    {
        for (int row = 0; row < 7; row++)
        {
            int bit = (ch[col] >> row) & 0x01;
            draw_pixel(x + col, row, bit);
        }
    }
}

void draw_text(int x, const char *text)
{
    int cursor = x;

    for (int i = 0; i < strlen(text); i++)
    {
        draw_char(cursor, text[i]);
        cursor += 6;
    }
}

// EMOJI
void draw_emoji(int x, uint8_t *emoji)
{
    for (int row = 0; row < 8; row++)
    {
        for (int col = 0; col < 8; col++)
        {
            int bit = (emoji[row] >> (7 - col)) & 1;
            draw_pixel(x + col, row, bit);
        }
    }
}

// ACTUALIZAR DISPLAY
void max7219_update()
{
    for (int row = 0; row < 8; row++)
    {
        uint8_t values[NUM_MODULES];

        for (int m = 0; m < NUM_MODULES; m++)
        {
            uint8_t value = 0;
            for (int bit = 0; bit < 8; bit++)
            {
                int x = m * 8 + bit;
                value |= (framebuffer[row][x] << (7 - bit));
            }
            values[NUM_MODULES - 1 - m] = value;
        }
        max7219_send_all(row + 1, values);
    }
}

// SCROLL
void scroll_text(const char *text)
{
    int len = strlen(text) * 6;
    for (int offset = WIDTH; offset > -len; offset--)
    {
        clear_buffer();
        draw_text(offset, text);
        max7219_update();
        vTaskDelay(pdMS_TO_TICKS(40));
    }
}

// TEST
void test_all_on()
{
    uint8_t data[NUM_MODULES];
    memset(data, 0xFF, NUM_MODULES);
    for (int row = 1; row <= 8; row++)
    {
        max7219_send_all(row, data);
    }
}

// MAIN
void app_main(void)
{
    spi_bus_config_t buscfg = {
        .mosi_io_num = PIN_NUM_MOSI,
        .miso_io_num = -1,
        .sclk_io_num = PIN_NUM_CLK,
        .quadwp_io_num = -1,
        .quadhd_io_num = -1,
    };
    spi_device_interface_config_t devcfg = {
        .clock_speed_hz = 100000, // velocidad baja
        .mode = 0,
        .spics_io_num = -1,
        .queue_size = 1,
    };
    spi_bus_initialize(SPI2_HOST, &buscfg, SPI_DMA_CH_AUTO);
    spi_bus_add_device(SPI2_HOST, &devcfg, &spi);
    gpio_set_direction(PIN_NUM_CS, GPIO_MODE_OUTPUT);
    gpio_set_level(PIN_NUM_CS, 1);
    max7219_init();

    while (1)
    {
        // PRUEBA
        test_all_on();
        vTaskDelay(pdMS_TO_TICKS(1000));
        // SCROLL;
        scroll_text("Internet of Things 2026-1");
        clear_buffer();
        scroll_text("Perception Layer +*/?");
        // EMOJI
        clear_buffer();
        draw_emoji(12, emoji_smile);
        max7219_update();
        vTaskDelay(pdMS_TO_TICKS(2000));
    }
}
